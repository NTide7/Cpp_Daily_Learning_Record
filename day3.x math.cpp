#include <iostream>
#include "math.h"

void numPlus (int a , int b) //完成加法并输出
    {
        std::cout << "数字 " << a << " 和 " << b << " 的和是：" << a + b << '\n';
    }

void numTimes (int a , int b) //完成乘法并输出
    {
        std::cout << "数字 " << a << " 和 " << b << " 的积是：" << a * b << '\n';
    }